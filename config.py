# Configuração de conexão com o banco de dados MySQL

# O endereço do servidor de banco de dados. No caso, "localhost" indica que o banco de dados está rodando na mesma máquina do código.
DB_HOST = "localhost"

# Nome do usuário que será utilizado para se conectar ao banco de dados.
# No caso, estamos utilizando o usuário "root", que é o usuário padrão no MySQL.
DB_USER = "root"

# Senha do usuário utilizado para se conectar ao banco de dados.
# Neste exemplo, a senha está vazia (""), mas é altamente recomendado definir uma senha segura.
DB_PASSWORD = ""

# Nome do banco de dados que será acessado. Aqui, estamos configurando o banco "tca".
# Este banco de dados precisa existir no servidor MySQL especificado em DB_HOST.
DB_NAME = "tca"
